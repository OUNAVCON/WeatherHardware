int main(void) {
  /* Init board hardware. */
  BOARD_InitPins();
  BOARD_BootClockRUN();

  /* Create Queues */
  weatherMessageQueue = xQueueCreate( 5, sizeof( AWeatherMessage ) );

  if( weatherMessageQueue == NULL )
  {
      /* Queue was not created and must not be used. */
  }

  /* Create RTOS task */
  // use heartbeat with debug and maybe for future use.
  xTaskCreate(heartbeat_task, "HB", hello_task_STACK_SIZE, NULL, hello_task_PRIORITY, NULL);
  xTaskCreate(i2c_task, "px", i2c_task_STACK_SIZE, NULL, i2c_task_PRIORITY, NULL);
  xTaskCreate(lightSensor_task, "AmbL", lightSensor_task_STACK_SIZE, NULL, lightSensor_task_PRIORITY, NULL);
  xTaskCreate(weather_task, "WX", weather_task_STACK_SIZE, NULL, weather_task_PRIORITY, NULL);
  xTaskCreate(capture_task, "WS_RF", capture_task_STACK_SIZE, NULL, capture_task_PRIORITY, NULL);
  xTaskCreate(dht_task, "RH", dht_task_STACK_SIZE, NULL, dht_task_PRIORITY, NULL);

  vTaskStartScheduler();

  for(;;) { /* Infinite loop to avoid leaving the main function */
    __asm("NOP"); /* something to use as a breakpoint stop while looping */
  }
}




